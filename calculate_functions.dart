class CalculateFunctions {
  double? num1;
  double? num2;
  CalculateFunctions({this.num1, this.num2});

  double? add(double number1, number2) {
    return (number1 + number2);
  }

  double? subtract(double number1, number2) {
    return (number1 - number2);
  }

  double? divide(double number1, number2) {
    return (number1 / number2);
  }

  double? multiply(double number1, number2) {
    return (number1 * number2);
  }
}
